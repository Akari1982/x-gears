void
DialogFile::GetDialogs( const int file_id )
{
    Logger* export_script = new Logger( "exported/debug/0" + IntToString( file_id ) + "_7_dialogs.txt" );

    u32 number_of_dialogs = GetU32LE( 0x00 ) + 1;

    std::vector< unsigned char > dialog;
    dialog.push_back( 0xff );
    dialog.push_back( 0xfe );
    export_script->Log( dialog );
    dialog.clear();

    for( u32 i = 0; i < number_of_dialogs; ++i )
    {
        int size_offset = 0x04 + number_of_dialogs * 0x02 + i * 0x02;
        int data_offset = GetU16LE( 0x04 + i * 0x02 );

        export_script->Log( dialog );
        dialog.clear();
        export_script->LogW( "<Dialog id=0x" + HexToString( i, 2, '0' ) + " " );
        export_script->LogW( "width=" + IntToString( GetU8( size_offset + 0 ) ) + ", height=" + IntToString( GetU8( size_offset + 1 ) ) + ">\n" );

        while( true )
        {
            u8 opcode = GetU8( data_offset );
            data_offset += 1;
            if( opcode == 0x00 )
            {
                export_script->LogW( "\n<End>\n" );
                break;
            }
            else if( opcode == 0x01 )
            {
                export_script->LogW( "\n" );
            }
            else if( opcode == 0x02 )
            {
                export_script->LogW( "\n<Window>\n" );
            }
            else if( opcode == 0x0f )
            {
                u8 sub_opcode = GetU8( data_offset );
                data_offset += 1;
                if( sub_opcode == 0x00 )
                {
                    export_script->LogW( "<SpecialWait wait=\"" + HexToString( GetU8( data_offset ), 2, '0' ) + "\">" );
                    data_offset += 1;
                }
                else if( sub_opcode == 0x01 )
                {
                    export_script->LogW( "<SpecialLettersToRead number=\"" + HexToString( GetU8( data_offset ), 2, '0' ) + "\">" );
                    data_offset += 1;
                }
                else if( sub_opcode == 0x02 )
                {
                    export_script->LogW( "<SpecialWaitAndClose wait=\"" + HexToString( GetU8( data_offset ), 2, '0' ) + "\">" );
                    data_offset += 1;
                }
                else if( sub_opcode == 0x05 )
                {
                    export_script->LogW( "<SpecialCharacter char_id=\"" + HexToString( GetU8( data_offset ), 2, '0' ) + "\">" );
                    data_offset += 1;
                }
                else
                {
                    export_script->LogW( "<special>" + HexToString( sub_opcode, 2, '0' ) + "\n" );
                    break;
                }
            }
            else if( opcode < 0xf8 )
            {
                if( japanese_chars[ opcode ] != 0x0000 )
                {
                    dialog.push_back( japanese_chars[ opcode ] >> 8 );
                    dialog.push_back( japanese_chars[ opcode ] & 0xff );
                    export_script->Log( dialog );
                    dialog.clear();
                }
                else
                {
                    export_script->LogW( "[MISSING 0x" + HexToString( opcode, 2, '0' ) + "]" );
                }
            }
            else if( opcode == 0xf8 )
            {
                u8 sub_opcode = GetU8( data_offset );
                data_offset += 1;
                if( japanese_chars_f8[ sub_opcode ] != 0x0000 )
                {
                    dialog.push_back( japanese_chars_f8[ sub_opcode ] >> 8 );
                    dialog.push_back( japanese_chars_f8[ sub_opcode ] & 0xff );
                    export_script->Log( dialog );
                    dialog.clear();
                }
                else
                {
                    export_script->LogW( "[MISSING 0xF8" + HexToString( sub_opcode, 2, '0' ) + "]" );
                }
            }
            else if( opcode == 0xf9 )
            {
                u8 sub_opcode = GetU8( data_offset );
                data_offset += 1;
                if( japanese_chars_f9[ sub_opcode ] != 0x0000 )
                {
                    dialog.push_back( japanese_chars_f9[ sub_opcode ] >> 8 );
                    dialog.push_back( japanese_chars_f9[ sub_opcode ] & 0xff );
                    export_script->Log( dialog );
                    dialog.clear();
                }
                else
                {
                    export_script->LogW( "[MISSING 0xF9" + HexToString( sub_opcode, 2, '0' ) + "]" );
                }
            }
            else if( opcode == 0xfa )
            {
                u8 sub_opcode = GetU8( data_offset );
                data_offset += 1;
                if( japanese_chars_fa[ sub_opcode ] != 0x0000 )
                {
                    dialog.push_back( japanese_chars_fa[ sub_opcode ] >> 8 );
                    dialog.push_back( japanese_chars_fa[ sub_opcode ] & 0xff );
                    export_script->Log( dialog );
                    dialog.clear();
                }
                else
                {
                    export_script->LogW( "[MISSING 0xFA" + HexToString( sub_opcode, 2, '0' ) + "]" );
                }
            }
            else if( opcode == 0xfb )
            {
                u8 sub_opcode = GetU8( data_offset );
                data_offset += 1;
                if( japanese_chars_fb[ sub_opcode ] != 0x0000 )
                {
                    dialog.push_back( japanese_chars_fb[ sub_opcode ] >> 8 );
                    dialog.push_back( japanese_chars_fb[ sub_opcode ] & 0xff );
                    export_script->Log( dialog );
                    dialog.clear();
                }
                else
                {
                    export_script->LogW( "[MISSING 0xFB" + HexToString( sub_opcode, 2, '0' ) + "]" );
                }
            }
            else if( opcode == 0xfc )
            {
                u8 sub_opcode = GetU8( data_offset );
                data_offset += 1;
                if( japanese_chars_fc[ sub_opcode ] != 0x0000 )
                {
                    dialog.push_back( japanese_chars_fc[ sub_opcode ] >> 8 );
                    dialog.push_back( japanese_chars_fc[ sub_opcode ] & 0xff );
                    export_script->Log( dialog );
                    dialog.clear();
                }
                else
                {
                    export_script->LogW( "[MISSING 0xFC" + HexToString( sub_opcode, 2, '0' ) + "]" );
                }
            }
            else if( opcode == 0xfd )
            {
                u8 sub_opcode = GetU8( data_offset );
                data_offset += 1;
                if( japanese_chars_fd[ sub_opcode ] != 0x0000 )
                {
                    dialog.push_back( japanese_chars_fd[ sub_opcode ] >> 8 );
                    dialog.push_back( japanese_chars_fd[ sub_opcode ] & 0xff );
                    export_script->Log( dialog );
                    dialog.clear();
                }
                else
                {
                    export_script->LogW( "[MISSING 0xFD" + HexToString( sub_opcode, 2, '0' ) + "]" );
                }
            }
            else
            {
                export_script->Log( dialog );
                dialog.clear();
                export_script->LogW( "[MISSING 0x" + HexToString( opcode, 2, '0' ) + "]" );
                break;
            }
        }

        export_script->LogW( "\n" );
    }
}
